package academy.learnprogramming;

// Int -
// Byte -

// Overflow / Underflow : When the variable exceeds that data type's max / min value.
// Be careful of overflow and underflow
// byte = 8 bits, short = 16bits, int = 32bits, long = 64bits
// byte < short < int < long
// to get min/ max value of data type use .MIN_VALUE / MAX_VALUE


public class Main {
    public static void main (String[] args) {
        byte myMaxByte = Byte.MAX_VALUE;
        byte myMinByte = Byte.MIN_VALUE;
        short myMaxByte = Short.MAX_VALUE;
        short myMinByte = Short.MIN_VALUE;
        int myMaxByte = Integer.MAX_VALUE;
        int myMinByte = Integer.MIN_VALUE;
        long myMaxByte = Long.MAX_VALUE;
        long myMinByte = Long.MIN_VALUE;
    }
}

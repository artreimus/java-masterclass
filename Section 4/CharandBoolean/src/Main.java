public class Main {

    // Char value must be declared using ' ' single quotes
    // Char can only store one letter / character
    // Char can be used to store Unicode
    public static void main(String[] args) {
        char myChar = 'D';
        char unicodeChar = '\u00A9'; // Unicode
        System.out.println(myChar);
        System.out.println(unicodeChar);
        boolean isCustomerOverTwentyOne = true;
    }
}